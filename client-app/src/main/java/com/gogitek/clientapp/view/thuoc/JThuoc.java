    public void initData() {
        List<Thuoc> list = new ArrayList<>();
        try {
            SocketConnection socketConnection = thuocService.findListThuocByKey("");
            list = JsonUtils.stringToArray(socketConnection.getResult().toString(), Thuoc[].class);
            socketConnection.closeConnection();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        DefaultTableModel model = (DefaultTableModel) tableThuoc.getModel();
        setTableData(list, model);
    }

    private void setTableData(List<Thuoc> list, DefaultTableModel model) {
        for (Thuoc value : list) {
            Object[] rowData = new Object[6];
            rowData[0] = value.getId();
            rowData[1] = value.getMaThuoc();
            rowData[2] = value.getTenThuoc();
            rowData[3] = value.getDangThuoc();
            rowData[4] = value.getGiaThuoc();
            rowData[5] = value.getCongDungId();
            model.addRow(rowData);
        }
    }

