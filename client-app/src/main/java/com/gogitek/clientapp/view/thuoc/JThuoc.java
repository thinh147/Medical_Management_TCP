    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        // TODO add your handling code here:        
        inputName.setText("");
        DefaultTableModel model = (DefaultTableModel) tableThuoc.getModel();
        model.setRowCount(0);
        List<Thuoc> list = new ArrayList<>();
        try {
            SocketConnection socketConnection = thuocService.findListThuocByKey("");
            list = JsonUtils.stringToArray(socketConnection.getResult().toString(), Thuoc[].class);
            socketConnection.closeConnection();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        setTableData(list, model);
    }//GEN-LAST:event_btnRefreshActionPerformed

